import UIKit

protocol ViewModel {}

class BaseViewController<V: ViewModel>: UIViewController {
    
    let viewModel: V
    
    init(viewModel: V) {
        self.viewModel = viewModel
        super.init(nibName: nil, bundle: nil)
    }
    
    required init?(coder aDecoder: NSCoder) {
        fatalError("init(coder:) has not been implemented")
    }
    
}
